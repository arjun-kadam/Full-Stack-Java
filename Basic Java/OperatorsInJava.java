public class OperatorsInJava {
    public static void main(String[] args) {
        int a=30,b=10;

        //Addition Operator
        System.out.println(a+b);

        // Substraction
        System.out.println(a-b);

        // Multiplication
        System.out.println(a*b);

        // Division
        System.out.println(a/b);

        // Increament 
        System.out.println(++a); 

        // Decreament
        System.out.println(--b);

    }
    
}
