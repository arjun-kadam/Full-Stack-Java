public class WhileLoop {
    public static void main(String[] args) {
        //While Loop
        int a=10;
        while (a>=0) {
            System.out.println(a);
            a--;
        }

        // Do While Loop
        int b=0;
        do {
            System.out.println(b);
            a++;
        } while (b>10);
    }
}
